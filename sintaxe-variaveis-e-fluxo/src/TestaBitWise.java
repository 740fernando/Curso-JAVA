
public class TestaBitWise {
public static void main(String[] args) {
	int n1 = 89;
	int n2 = 60;
	// aplicação comum : verificar bit
	// Executa a operação lógica bit a bit do número armazenado nas variaveis
	System.out.println(n1 & n2);
	System.out.println(n1 | n2);
	System.out.println(n1^n2);//XOR
}
}
